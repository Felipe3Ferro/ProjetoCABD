private void cadastrar() {
        try {
            MedicoVO mVO = new MedicoVO();
            mVO.setNome(jtNome.getText());
            mVO.setCpf(jtCpf.getText());
            mVO.setSexo(bgSexo.getSelection().getActionCommand());
            mVO.setArea(jcbArea.getSelectedItem().toString());
            mVO.setTelefone(Integer.parseInt(jtTelefone.getText()));
            mVO.setSalario(Double.parseDouble(jtSalario.getText()));
            
            //Enviando o objeto pVO para o banco de dados
            MedicoServicos ms = ServicosFactory.getMedicoServicos();
            //Chamando o m√©todo 
            ms.cadastrarMedico(mVO);
            
            JOptionPane.showMessageDialog(rootPane, 
                    "Medico cadastrado com sucesso!");
        } catch (Exception e) {
             e.printStackTrace();
            JOptionPane.showMessageDialog(
                    rootPane,
                    "Erro! "+e.getMessage(),
                    "erro",
                    JOptionPane.ERROR_MESSAGE);
        }//fecha catch
    }//fim cadastrar
     private void limpar() {
        jtNome.setText("");
        jtCpf.setText("");
        jtSalario.setText("");
        jtTelefone.setText("");
        bgSexo.clearSelection();
        jcbArea.setSelectedItem(null);
        
    }//fecha limpar   

